# Arthmetic Function

import numpy as np 

arr = np.array([1,2,3,5,94,2,0])

print("Max : ",np.max(arr))
print("Min : ",np.min(arr))
print("argmin : ",np.argmin(arr))
print("argmax : ",np.argmax(arr))


arr2 = np.array([1,4,9])
print("Sqrt : ",np.sqrt(arr2))

arr3 = np.array([1,2,3,4])
print("sin : ",np.sin(arr3))
print("cos : ",np.cos(arr3))

print("cumsum : ",np.cumsum(arr3))